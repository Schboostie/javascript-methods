const str1 = "Hello";
const str2 = "world!";
const result = str1 + " " + str2;
console.log(result); // Output: "Hello world!"


/*In the above example, we define two string variables str1 and str2. 
Then we use the + operator to concatenate them along with a space character between them to create a new string called result. 
Finally, we print out the concatenated string using console.log().*/


